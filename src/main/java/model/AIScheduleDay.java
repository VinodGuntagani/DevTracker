package model;

import java.util.List;

public class AIScheduleDay {

	private int day;

	private List<Integer> tasks;

	public int getDay() {

		return day;

	}

	public void setDay(int day) {

		this.day = day;

	}

	public List<Integer> getTasks() {

		return tasks;

	}

	public void setTasks(List<Integer> tasks) {

		this.tasks = tasks;

	}

}